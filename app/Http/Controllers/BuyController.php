    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request, [
            'category' => 'required',
            'client_id' => 'required',
            'inventory' => 'required',
            'payment_type' => 'required'
        ]);

        $buy = new Buy;
        $buy->user_id = $request->input('client_id');
        $buy->user_category = $request->input('category');
        $buy->item_name = $request->input('inventory');
        $buy->payment_type = $request->input('payment_type');
        $buy->save();

        return redirect("/admin/sales");
    }

